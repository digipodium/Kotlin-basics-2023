package simple.examples

fun main(){
    val a: Int = 10 // explicit type declaration
    val b = 10 // implicit type declaration

    var name: String = "Zaid" // mutable variable
    var age = 20 // mutable variable

    println(a)
    println(b)
    println(name)
    println(age)
    name  = "Zaid Kamil"
    age = 31
    println(name)
    println(age)
}